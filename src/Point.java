public class Point {

    private Integer x;
    private Double y;
    private Integer r;

    public Integer getX ( ) {
        return x;
    }

    public void setX (Integer x) {
        this.x = x;
    }

    public Double getY ( ) {
        return y;
    }

    public void setY (Double y) {
        this.y = y;
    }

    public Integer getR ( ) {
        return r;
    }

    public void setR (Integer r) {
        this.r = r;
    }
}
