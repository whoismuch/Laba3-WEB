public class Point {

    private Integer x;
    private Double y;
    private Integer r;
    private Boolean result;

    public Boolean getResult ( ) {
        return result;
    }

    public void setResult (Boolean result) {
        this.result = result;
    }

    public Integer getX ( ) {
        return x;
    }

    public void setX (Integer x) {
        this.x = x;
    }

    public Double getY ( ) {
        return y;
    }

    public void setY (Double y) {
        this.y = y;
    }

    public Integer getR ( ) {
        return r;
    }

    public void setR (Integer r) {
        this.r = r;
    }
}
